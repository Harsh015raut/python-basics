#List Methods
l1 = [1,8,7,2,21,15]
print(l1)
l1.sort()#Sorts list in ascending order
print(l1)
l1.reverse()#Reverses list
print(l1)  
l1.append(44) #Adds(appends) element at the end of the list
print(l1)
l1.insert(3,5) #5 will be added at index 3
print(l1)
l1.pop(3) # It will remove the element at specified index
print(l1)
l1.remove(15) #It removes the element specified
print(l1)
l2 = [5,7,89,65]
print(l1)
l1.extend(l2)#Adds whole list in list
print(l1)
